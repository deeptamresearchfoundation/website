var a;

function show_more() {
    if (a == 0) {
        document.getElementById("showmore").style.display = "none";
        document.getElementById("showhideka").innerText = "Show More";
        return a = 1;
    }

    else {
        document.getElementById("showmore").style.display = "block";
        document.getElementById("showhideka").innerText = "Hide Content";
        return a = 0;
    }
}