function myFunction() {
    var copyText = document.getElementById("myInput");
    copyText.select();
    copyText.setSelectionRange(0, 9999999999)
    document.execCommand("copy");
    window.getSelection().removeAllRanges()
    // alert("Copied the text: " + copyText.value);
}

